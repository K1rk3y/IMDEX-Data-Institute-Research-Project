    def special_initialize(self):
        """Initialize audio base embeddings from text while keeping conv layers random"""
        # 1. Copy token type embeddings
        self.audio_token_type.weight.data.copy_(
            self.text_token_type.weight.data
        )

        # 2. Handle position embeddings based on sharing strategy
        if not self.share_base_pos:
            # Copy text base position embeddings to audio base positions
            self.audio_position_embeddings.weight.data.copy_(
                self.text_position_embeddings.weight.data
            )

        # 3. Initialize audio projection from first 512 text embeddings
        with torch.no_grad():
            # Get first 512 text embeddings (matches projection input dim)
            text_emb_subset = self.word_embeddings.weight.data[:512]
            
            # Initialize projection weights as transpose of text embeddings
            self.audio_projection.weight.data.copy_(text_emb_subset)
            self.audio_projection.bias.data.zero_()

        # 4. Conv positional encoders remain with default initialization

    def _get_position_embeddings(self, position_ids, modality='text'):
        """Handle base position embedding selection"""
        if self.share_base_pos:
            return self.position_embeddings(position_ids)
        else:
            emb_layer = (self.text_position_embeddings if modality == 'text'
                         else self.audio_position_embeddings)
            return emb_layer(position_ids)

    def _process_text(self, input_ids, padding_mask, token_type_ids=None, mask_channel_indices=None):
        seq_length = input_ids.size(1)
        position_ids = torch.arange(seq_length, device=input_ids.device)
        
        # Base embeddings
        features = self.word_embeddings(input_ids)
        unmasked_features = features.clone()

        if self.mask:
            x, mask_indices, masked_lm_positions, masked_lm_labels = self.apply_mask_text(
                features,
                input_ids,
                padding_mask,
                mask_channel_indices
            )
            if not is_xla_tensor(x) and mask_indices is not None:
                # tpu-comment: reducing the size in a dynamic way causes
                # too many recompilations on xla.
                y = unmasked_features[mask_indices].view(
                    unmasked_features.size(0), -1, unmasked_features.size(-1)
                )
            else:
                y = unmasked_features
        else:
            x = features
            y = unmasked_features
            mask_indices = None

        pos_emb = self._get_position_embeddings(position_ids, 'text').unsqueeze(0)
        
        # Token type
        token_type_emb = self.text_token_type(
            token_type_ids if token_type_ids is not None 
            else torch.zeros_like(input_ids)
        )

        # Conv positional modulation (random init preserved)
        conv_pos = self.text_conv_pos(pos_emb.transpose(1, 2)).transpose(1, 2)
        
        return x + pos_emb + token_type_emb + conv_pos, y, padding_mask, mask_indices, masked_lm_positions, masked_lm_labels

    def _process_audio(self, audio_input, padding_mask=None, token_type_ids=None, mask_channel_indices=None):
        # Feature extraction

        if self.feature_grad_mult > 0:
            features = self.feature_extractor(audio_input)
            if self.feature_grad_mult != 1.0:
                features = GradMultiply.apply(features, self.feature_grad_mult)
        else:
            with torch.no_grad():
                features = self.feature_extractor(audio_input)

        self.features_pen = features.float().pow(2).mean()

        features = features.transpose(1, 2)
        features = self.LayerNorm(features)
        unmasked_features = features.clone()

        if padding_mask is not None and padding_mask.any():
            input_lengths = (1 - padding_mask.long()).sum(-1)
            # apply conv formula to get real output_lengths
            output_lengths = self._get_feat_extract_output_lengths(input_lengths)

            padding_mask = torch.zeros(
                features.shape[:2], dtype=features.dtype, device=features.device
            )

            # these two operations makes sure that all values
            # before the output lengths indices are attended to
            padding_mask[
                (
                    torch.arange(padding_mask.shape[0], device=padding_mask.device),
                    output_lengths - 1,
                )
            ] = 1
            padding_mask = (1 - padding_mask.flip([-1]).cumsum(-1).flip([-1])).bool()
        else:
            padding_mask = None

        time_steps_to_drop = features.size(1) % self.crop_seq_to_multiple
        if time_steps_to_drop != 0:
            features = features[:, :-time_steps_to_drop]
            unmasked_features = unmasked_features[:, :-time_steps_to_drop]
            if padding_mask is not None:
                padding_mask = padding_mask[:, :-time_steps_to_drop]

        if self.post_extract_proj is not None:
            features = self.post_extract_proj(features)

        features = self.dropout_input(features)
        unmasked_features = self.dropout_features(unmasked_features)

        num_vars = None
        code_ppl = None
        prob_ppl = None
        curr_temp = None

        if self.input_quantizer:
            q = self.input_quantizer(features, produce_targets=False)
            features = q["x"]
            num_vars = q["num_vars"]
            code_ppl = q["code_perplexity"]
            prob_ppl = q["prob_perplexity"]
            curr_temp = q["temp"]
            features = self.project_inp(features)

        if self.mask:
            x, mask_indices = self.apply_mask_audio(
                features,
                padding_mask,
                mask_indices=mask_indices,
                mask_channel_indices=mask_channel_indices,
            )
            if not is_xla_tensor(x) and mask_indices is not None:
                # tpu-comment: reducing the size in a dynamic way causes
                # too many recompilations on xla.
                y = unmasked_features[mask_indices].view(
                    unmasked_features.size(0), -1, unmasked_features.size(-1)
                )
            else:
                y = unmasked_features
        else:
            x = features
            y = unmasked_features
            mask_indices = None


        # Position handling
        seq_length = x.size(1)
        position_ids = torch.arange(seq_length, device=audio_input.device)
        pos_emb = self._get_position_embeddings(position_ids, 'audio').unsqueeze(0)
        
        # Token type
        token_type_emb = self.audio_token_type(
            token_type_ids if token_type_ids is not None 
            else torch.ones(x.shape[:2], device=x.device, dtype=torch.long)
        )

        # Conv positional modulation (random init preserved)
        conv_pos = self.audio_conv_pos(pos_emb.transpose(1, 2)).transpose(1, 2)
        
        return x + pos_emb + token_type_emb + conv_pos, y, padding_mask, mask_indices, (num_vars, code_ppl, prob_ppl, curr_temp)

    def apply_mask_text(self, x, tokens, padding_mask, mask_channel_indices=None, min_masks=2):
        device = x.device
        B, T, C = x.shape

        # Create padding mask from input_ids if not provided
        if padding_mask is None:
            special_tokens = [self.cls_token_id, self.pad_token_id]
            padding_mask = torch.isin(
                tokens, 
                torch.tensor(special_tokens, device=device)
            )

        # Check maskable positions
        maskable_positions = (~padding_mask).sum().item()
        if maskable_positions == 0:
            raise ValueError("No maskable positions in the sequence")

        if self.mask_channel_before:
            mask_channel_indices = compute_mask_indices(
                (B, C),
                None,
                self.mask_channel_prob,
                self.mask_channel_length,
                self.mask_channel_selection,
                self.mask_channel_other,
                no_overlap=self.no_mask_channel_overlap,
                min_space=self.mask_channel_min_space,
            )
            mask_channel_indices = (
                torch.from_numpy(mask_channel_indices)
                .to(x.device)
                .unsqueeze(1)
                .expand(-1, T, -1)
            )
            x[mask_channel_indices] = 0

        # Compute mask indices
        mask_indices = compute_mask_indices(
            shape=(B, T),
            padding_mask=padding_mask,
            mask_prob=self.mask_prob,
            mask_length=self.mask_length,
            mask_type=self.mask_selection,
            mask_other=self.mask_other,
            min_masks=min_masks,
            no_overlap=self.no_mask_overlap,
            min_space=self.mask_min_space,
            require_same_masks=self.config.require_same_masks,
            mask_dropout=self.config.mask_dropout,
        )

        # Convert to tensor and move to device
        mask_indices = torch.from_numpy(mask_indices).to(device)

        # Apply masking using index_put
        x = index_put(x.clone(), mask_indices, self.mask_emb_text)

        if not self.mask_channel_before:
            if mask_channel_indices is None:
                mask_channel_indices = compute_mask_indices(
                    (B, C),
                    None,
                    self.mask_channel_prob,
                    self.mask_channel_length,
                    self.mask_channel_selection,
                    self.mask_channel_other,
                    no_overlap=self.no_mask_channel_overlap,
                    min_space=self.mask_channel_min_space,
                )
                mask_channel_indices = (
                    torch.from_numpy(mask_channel_indices)
                    .to(x.device)
                    .unsqueeze(1)
                    .expand(-1, T, -1)
                )
            x = index_put(x, mask_channel_indices, 0)

        # Extract positions and labels DEPENDS ON THE OPT TYPE OF MASK_INDICES
        masked_lm_positions = [
            mask_indices[b].nonzero().squeeze(-1)
            for b in range(B)
        ]
        masked_lm_labels = [
            tokens[b, mask_indices[b]] 
            for b in range(B)
        ]

        return x, mask_indices, masked_lm_positions, masked_lm_labels

